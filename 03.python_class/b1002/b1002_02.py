# 숫자를 입력받아 100보다 큰수인지 100입니다. 작은수 인지 출력하시오.
num = int(input("숫자를 입력하세요."))
if num>100:
  print("100보다 큰수입니다.")
elif num == 100:
  print("100입니다.")  
else:
  print("100보다 작은수입니다.")  


